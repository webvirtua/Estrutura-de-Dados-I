void imprime_Fila(Fila* fi){
    if(fi == NULL)
        return;
    Elem* no = fi->inicio;
    while(no != NULL){
        printf("Identificacao do Aviao: %d\n",no->dados.numId);
        printf("Modelo do Aviao: %s\n",no->dados.nome);
        printf("Capacidade: %d pessoas\n",no->dados.capacidade);
        printf("-------------------------------\n");
        no = no->prox;
    }
}
